
// Calcula la suma de los numeros del 1 al 10

#include <iostream>

using namespace std;

int main()
{
   unsigned int suma = 0;
   unsigned int x = 1 ;

   while (x <11) // Ciclo del 1 al 10
    {
    suma += x;
    x ++;

    }
   cout << "La suma es :" << suma; // Mostramos el resultado
}





